package com.ps.citizen3.data.api.GoogleCivicModel;

import com.google.gson.annotations.Expose;

public class Channel {

    @Expose
    private String type;
    @Expose
    private String id;

    /**
     *
     * @return
     * The type
     */
    public String getType() {
        return type;
    }

    /**
     *
     * @param type
     * The type
     */
    public void setType(String type) {
        this.type = type;
    }

    /**
     *
     * @return
     * The id
     */
    public String getId() {
        return id;
    }

    /**
     *
     * @param id
     * The id
     */
    public void setId(String id) {
        this.id = id;
    }

}